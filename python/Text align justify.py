def justify_line(t, width):
    print('Justifying', t)
    words = t.split(' ')
    i = 0
    while sum([len(w) for w in words]) < width:
        words[i] = words[i] + ' '
        i = (i + 1) % (len(words) - 1)

    return ''.join(words)


def justify(text, width):
    if len(text) <= width:
        return text
    elif text[width] == ' ':
        return text[0:width] + '\n' + justify(text[width:].strip(), width)

    last_space_index = text.rfind(' ', 0, width)
    return justify_line(text[0: last_space_index].strip(), width) + '\n' + justify(text[last_space_index:].strip(), width)


